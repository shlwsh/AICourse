//
// 命令设计原则：
// 1. 所有命令都应该返回 Result<T, String> 类型
// 2. 错误信息应该清晰、具体，便于前端展示
// 3. 参数验证应该在命令层完成
// 4. 复杂的业务逻辑应该委托给 solver 或 db 模块
// 5. 所有命令都应该记录日志
